package com.zj.entity;

public class Black {
	
	private int id;
	private int userid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	@Override
	public String toString() {
		return "Black [id=" + id + ", userid=" + userid + "]";
	}
	
	

}
